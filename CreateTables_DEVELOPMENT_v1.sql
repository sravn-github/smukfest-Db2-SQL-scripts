CREATE PROCEDURE DEVELOPMENT.SMUK2023_FOOD_POST_ORDERS(IN p_JSON_SMUK_DATA
CLOB(1M))
LANGUAGE SQL
MODIFIES SQL DATA
BEGIN
    -- Place the original SQL query inside the stored procedure
    MERGE INTO DEVELOPMENT.SMUK2023_FOOD_ORDERS AS target
    USING (
        -- This query converts a JSON document containing meal information for volunteers into a relational table format.
        WITH JSON_SMUK_DATA (jsondoc) AS (
            VALUES (p_JSON_SMUK_DATA)
        ),
        N (num) AS (
            -- Numerical iterator to generate 100 dummy rows to join with the input JSON array
            -- This number can be increased or decreased, if we expect more or less than 100 elements.
            VALUES (0)
            UNION ALL
            SELECT num + 1
            FROM N
            WHERE num < 100
        ),
        K AS (
            -- Isolation of the outer-most key in the JSON object, which represents the MEMBA_ID
            -- The key is also used in the JSON Path expressions to access the array elements
            SELECT
                INT(REGEXP_EXTRACT(
                    CHAR(SUBSTR(JSON_QUERY(JSD.jsondoc, '$'), 4, LOCATE(':', JSON_QUERY(JSD.jsondoc, '$'), 4) - 6)),
                    '\d+'
                )) AS KEY
            FROM JSON_SMUK_DATA JSD
        ),
        TABLE_SMUK_DATA AS (
            SELECT
                -- Regular single-field variables
                K.KEY AS MEMBA_ID,
                JSON_VALUE(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].location') AS MEAL_LOCATION,
                JSON_VALUE(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].uuid')::VARCHAR(124)::BIGINT AS MEAL_UUID,
                TO_TIMESTAMP(REPLACE(REPLACE(JSON_VALUE(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].date'),'T',' '),'00Z',''),'YYYY-MM-DD HH24:MI:SS') AS MEAL_DATE,
            
                -- Food servings
                CASE WHEN LOCATE('breakfast', JSON_QUERY(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].servings')) > 0 THEN 1 ELSE 0 END AS CHECKED_BREAKFAST,
                CASE WHEN LOCATE('lunch',     JSON_QUERY(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].servings')) > 0 THEN 1 ELSE 0 END AS CHECKED_LUNCH,
                CASE WHEN LOCATE('dinner',    JSON_QUERY(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].servings')) > 0 THEN 1 ELSE 0 END AS CHECKED_DINNER,
                
                -- Food preferences
                CASE WHEN LOCATE('glutenfri',      JSON_QUERY(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].meal_preferences')) > 0 THEN 1 ELSE 0 END AS GLUTENFRI,
                CASE WHEN LOCATE('laktosefri',     JSON_QUERY(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].meal_preferences')) > 0 THEN 1 ELSE 0 END AS LAKTOSEFRI,
                CASE WHEN LOCATE('vegetarisk',     JSON_QUERY(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].meal_preferences')) > 0 THEN 1 ELSE 0 END AS VEGETARISK,
                CASE WHEN LOCATE('vegansk',        JSON_QUERY(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].meal_preferences')) > 0 THEN 1 ELSE 0 END AS VEGANSK,
                CASE WHEN LOCATE('minus_svinekod', JSON_QUERY(JSD.jsondoc, '$.' || K.KEY || '[' || RTRIM(CHAR(N.num)) || '].meal_preferences')) > 0 THEN 1 ELSE 0 END AS MINUS_SVINEKOD
            FROM JSON_SMUK_DATA JSD, N, K
        ) 
        SELECT 
            MEAL_UUID, MEMBA_ID, MEAL_DATE, MEAL_LOCATION, CHECKED_BREAKFAST, CHECKED_LUNCH, CHECKED_DINNER
        FROM 
            TABLE_SMUK_DATA
        WHERE 
            MEAL_UUID IS NOT NULL) as source
        ON target.MEAL_UUID = source.MEAL_UUID
    WHEN MATCHED THEN
        UPDATE SET target.MEMBA_ID = source.MEMBA_ID, 
            target.MEAL_DATE = source.MEAL_DATE, 
            target.MEAL_LOCATION = source.MEAL_LOCATION, 
            target.CHECKED_BREAKFAST = source.CHECKED_BREAKFAST, 
            target.CHECKED_LUNCH = source.CHECKED_LUNCH, 
            target.CHECKED_DINNER = source.CHECKED_DINNER
    WHEN NOT MATCHED THEN
        INSERT (MEAL_UUID, MEMBA_ID, MEAL_DATE, MEAL_LOCATION, CHECKED_BREAKFAST, CHECKED_LUNCH, CHECKED_DINNER) 
        VALUES (source.MEAL_UUID, source.MEMBA_ID, source.MEAL_DATE, source.MEAL_LOCATION, source.CHECKED_BREAKFAST, source.CHECKED_LUNCH, source.CHECKED_DINNER);--
END;--;

CREATE PROCEDURE DEVELOPMENT.SMUK2023_FOOD_GET_ORDERS(IN p_MEMBA_ID
INTEGER)
    RESULT SETS 1
    LANGUAGE SQL
BEGIN
    DECLARE cur CURSOR WITH RETURN FOR
        WITH json_data AS (
            SELECT VARCHAR(JSON_OBJECT(
                KEY 'uuid' VALUE food_orders.MEAL_UUID,
                KEY 'date' VALUE CONCAT(REPLACE(TO_CHAR(food_orders.MEAL_DATE, 'DD.MM.YYYY HH24:MI:SS.FF2'),' ','T'),'Z'),
                KEY 'servings' VALUE VARCHAR(JSON_ARRAY(
                    CASE WHEN food_orders.CHECKED_BREAKFAST = 1 THEN 'breakfast' ELSE NULL END,
                    CASE WHEN food_orders.CHECKED_LUNCH = 1 THEN 'lunch' ELSE NULL END,
                    CASE WHEN food_orders.CHECKED_DINNER = 1 THEN 'dinner' ELSE NULL END)),
                KEY 'location' VALUE food_orders.MEAL_LOCATION,
                KEY 'createdAt' VALUE CONCAT(REPLACE(TO_CHAR(food_orders.CREATED_AT, 'DD.MM.YYYY HH24:MI:SS.FF2'),' ','T'),'Z'),
                KEY 'lastUpdatedAt' VALUE CONCAT(REPLACE(TO_CHAR(food_orders.UPDATED_AT, 'DD.MM.YYYY HH24:MI:SS.FF2'),' ','T'),'Z')
            )) as json_object
        FROM
            DEVELOPMENT.SMUK2023_FOOD_ORDERS AS food_orders
        WHERE
            food_orders.MEMBA_ID = p_MEMBA_ID
        )
        SELECT
            '[' ||
