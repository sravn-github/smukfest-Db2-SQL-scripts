CREATE PROCEDURE DEVELOPMENT.SMUK2023_FOOD_GET_PREFERENCES(IN
p_MEMBA_ID INTEGER)
    RESULT SETS 1
    LANGUAGE SQL
BEGIN
    DECLARE cur CURSOR WITH RETURN FOR
        WITH json_data AS (
            SELECT VARCHAR(JSON_OBJECT(
                KEY 'meal_preferences' VALUE VARCHAR(JSON_ARRAY(
                    CASE WHEN preferences.GLUTENFRI = 1 THEN 'glutenfri' ELSE NULL END,
                    CASE WHEN preferences.LAKTOSEFRI = 1 THEN 'laktosefri' ELSE NULL END,
                    CASE WHEN preferences.VEGETARISK = 1 THEN 'vegetarisk' ELSE NULL END,
                    CASE WHEN preferences.VEGANSK = 1 THEN 'vegansk' ELSE NULL END,
                    CASE WHEN preferences.MINUS_SVINEKOD = 1 THEN 'minus_svinekod' ELSE NULL END)),
                KEY 'createdAt' VALUE CONCAT(REPLACE(TO_CHAR(preferences.CREATED_AT, 'DD.MM.YYYY HH24:MI:SS.FF2'),' ','T'),'Z'),
                KEY 'lastUpdatedAt' VALUE CONCAT(REPLACE(TO_CHAR(preferences.UPDATED_AT, 'DD.MM.YYYY HH24:MI:SS.FF2'),' ','T'),'Z')
            )) as json_object
        FROM
            DEVELOPMENT.SMUK2023_FOOD_PREFERENCES AS preferences
        WHERE
            preferences.MEMBA_ID = p_MEMBA_ID
        )
        SELECT
            REPLACE(REPLACE(REPLACE(json_object, '"[', '['), ']"', ']'), '\"', '"') AS result
        FROM
            json_data;
    OPEN cur;
END;;

