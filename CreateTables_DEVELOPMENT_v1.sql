    AFTER INSERT ON FOOD_ORDERS
    REFERENCING NEW AS new_row
    FOR EACH ROW
    BEGIN ATOMIC
        IF NOT EXISTS (SELECT 1 FROM FOOD_PREFERENCES WHERE MEMBA_ID = new_row.MEMBA_ID) THEN
            INSERT INTO FOOD_PREFERENCES (
                MEMBA_ID
            )
            VALUES (
                new_row.MEMBA_ID
           );
        END IF;
    END;

